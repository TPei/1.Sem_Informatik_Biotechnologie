package aufgaben.zettel2;

/**
 * Schreiben Sie ein Programm, das die Werte zweier Integer-Variablen x und y untereinander vertauscht.
 */
public class Aufgabe5 {

	public static void main(String[] args) {
		// numbers to swap
		int x = 10;
		int y = 11;
		
		// buffer variable
		int z = x;
		x = y;
		y = z;
		
	}

}
